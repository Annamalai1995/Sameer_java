package Fundamental;

public class Datatype {
    public static void main(String[] args) {
        int briyani = 450;
        float raitha = 15.5f;
        String name="Sameer";
        char initial='A';
        double amount=4500.00;
        System.out.println("Integer value"+briyani);
        System.out.println("Float value:"+raitha);
        System.out.println("String value:"+name);
        System.out.println("Character value:"+initial);
        System.out.println("Double value:"+amount);
        System.out.println("Double value:"+amount);
    }
}