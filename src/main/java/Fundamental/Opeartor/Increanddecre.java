package Fundamental.Opeartor;

public class Increanddecre {
    public static void main(String[] args) {
        int alpha=1000;
        int beta=999;
        //pre increment
        System.out.println(++alpha);
        System.out.println(--beta);
        //post increment
        System.out.println(alpha++);
        System.out.println(beta--);
    }
}
